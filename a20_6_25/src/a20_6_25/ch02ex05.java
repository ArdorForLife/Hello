package a20_6_25;
public class ch02ex05 {
	public static void main(String[] args) {
		boolean a;			//boolean 값은 true와 false 2가지이다.
		a=10!=10;			//true인지 false인지를 a방에 넣어준다. =표시는 대입해주는것. true false 묻고싶거든 ==으로 쓰면 됨. 같지않다의 등호에 있는 슬러시 기호는 !으로 쓴다 따라서 같지 않다는 !=으로 쓴다
		System.out.println(a);
		System.out.println(10!=10);
		System.out.println(10>=5);
		System.out.println(5<=3);
		System.out.println(10==10);
	}
}
